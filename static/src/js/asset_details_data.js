export const assetDetailsByCode = {};

export function buildAssetDetails(row = {}) {
    return {
        id: row.id || false,
        imageUrl: row.imageUrl || "",
        code: row.code || "-",
        listCode: row.code || "-",
        name: row.name || "-",
        activeState: row.active === false || row.status === "Retired" ? "Inactive" : "Active",
        active: row.active !== false,
        serial: row.serial || "-",
        category: row.category || "-",
        brand: row.brand || "-",
        assetType: row.assetType || "Tangible",
        status: row.status || "Available",
        statusTone: row.tone || "green",
        barcode: row.barcode || row.serial || "-",
        condition: row.condition || (row.status === "Retired" ? "Used" : "New"),
        description: row.description || row.name || "-",
        tagsNotes: row.tagsNotes || "",
        supplier: row.supplier || row.vendor || "-",
        invoiceNumber: row.invoiceNumber || "-",
        poNumber: row.poNumber || "-",
        purchaseDate: row.purchaseDate || "-",
        purchasePrice: row.price || "-",
        purchasePriceShort: row.price || "-",
        currentValue: row.price || "-",
        accumulatedDepreciation: "-",
        warrantyExpiry: row.warrantyExpiry || "-",
        expectedReturn: row.expectedReturnDate || "-",
        usefulLife: row.usefulLife || "-",
        depreciationMethod: row.depreciationMethod || "straight_line",
        residualValue: row.residualValue || 0,
        depreciationStartDate: row.depreciationStartDate || "-",
        invoiceFile: row.invoiceNumber || "-",
        assignment: {
            assignedTo: row.assignedTo || "-",
            assignedToId: row.assignedToId || false,
            department: row.assignedMeta || "-",
            employeeId: row.employeeCode || "-",
            assignDate: row.assignDate || "-",
            expectedReturn: row.expectedReturnDate || "-",
        },
        location: {
            location: row.location || "-",
            locationId: row.locationId || false,
            buildingFloor: row.buildingFloor || "-",
            roomArea: row.roomArea || "-",
            department: row.department || row.locationMeta || "-",
        },
        maintenanceRows: [],
    };
}
